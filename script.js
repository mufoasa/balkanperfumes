// Current language
let currentLang = "sq"

let currentGenderFilter = "all"
let currentBrandFilter = "all"

// Language switcher
document.addEventListener("DOMContentLoaded", () => {
  const langButtons = document.querySelectorAll(".lang-btn")
  const mobileMenuBtn = document.querySelector(".mobile-menu-btn")
  const navLinks = document.querySelector(".nav-links")

  // Language switching
  langButtons.forEach((btn) => {
    btn.addEventListener("click", () => {
      const lang = btn.getAttribute("data-lang")
      switchLanguage(lang)

      langButtons.forEach((b) => b.classList.remove("active"))
      btn.classList.add("active")
    })
  })

  // Mobile menu toggle
  if (mobileMenuBtn) {
    mobileMenuBtn.addEventListener("click", () => {
      mobileMenuBtn.classList.toggle("active")
      navLinks.classList.toggle("active")
    })

    // Close menu when clicking a link
    document.querySelectorAll(".nav-links a").forEach((link) => {
      link.addEventListener("click", () => {
        mobileMenuBtn.classList.remove("active")
        navLinks.classList.remove("active")
      })
    })
  }

  // Contact form submission
  const contactForm = document.querySelector(".contact-form")
  if (contactForm) {
    contactForm.addEventListener("submit", (e) => {
      e.preventDefault()
      alert(translations[currentLang]["form-submit"] || "Message sent successfully!")
      contactForm.reset()
    })
  }

  // Smooth scrolling for anchor links
  document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
    anchor.addEventListener("click", function (e) {
      e.preventDefault()
      const target = document.querySelector(this.getAttribute("href"))
      if (target) {
        target.scrollIntoView({
          behavior: "smooth",
          block: "start",
        })
      }
    })
  })

  // Navbar background on scroll
  const navbar = document.querySelector(".navbar")
  window.addEventListener("scroll", () => {
    if (window.scrollY > 100) {
      navbar.style.background = "rgba(10, 10, 10, 0.98)"
    } else {
      navbar.style.background = "rgba(10, 10, 10, 0.95)"
    }
  })

  // Product filters
  const genderFilters = document.querySelectorAll(".filter-btn[data-filter]")
  const brandFilters = document.querySelectorAll(".filter-btn[data-brand]")
  const productCards = document.querySelectorAll(".product-card")

  // Gender filter
  genderFilters.forEach((btn) => {
    btn.addEventListener("click", () => {
      currentGenderFilter = btn.getAttribute("data-filter")

      // Update active state
      genderFilters.forEach((b) => b.classList.remove("active"))
      btn.classList.add("active")

      // Apply filters
      filterProducts()
    })
  })

  // Brand filter
  brandFilters.forEach((btn) => {
    btn.addEventListener("click", () => {
      currentBrandFilter = btn.getAttribute("data-brand")

      // Update active state
      brandFilters.forEach((b) => b.classList.remove("active"))
      btn.classList.add("active")

      // Apply filters
      filterProducts()
    })
  })

  // Filter products function
  function filterProducts() {
    productCards.forEach((card) => {
      const productGender = card.getAttribute("data-gender")
      const productBrand = card.getAttribute("data-brand")

      const genderMatch = currentGenderFilter === "all" || productGender === currentGenderFilter
      const brandMatch = currentBrandFilter === "all" || productBrand === currentBrandFilter

      if (genderMatch && brandMatch) {
        card.style.display = "block"
        // Add fade-in animation
        card.style.animation = "fadeInUp 0.5s ease"
      } else {
        card.style.display = "none"
      }
    })
  }

  // Initialize with default language
  switchLanguage(currentLang)
})

// Switch language function
function switchLanguage(lang) {
  currentLang = lang
  document.documentElement.lang = lang

  // Update all elements with data-translate attribute
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate")
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key]
    }
  })

  // Update placeholder attributes
  document.querySelectorAll("[data-translate-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-translate-placeholder")
    if (translations[lang] && translations[lang][key]) {
      element.placeholder = translations[lang][key]
    }
  })
}
